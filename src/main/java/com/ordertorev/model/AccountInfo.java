package com.ordertorev.model;

public class AccountInfo {

	public Integer accoutId;
	public String accoutName;
	public String accoutDesc;
	public Double amount;
	public Integer getAccoutId() {
		return accoutId;
	}
	public void setAccoutId(Integer accoutId) {
		this.accoutId = accoutId;
	}
	public String getAccoutName() {
		return accoutName;
	}
	public void setAccoutName(String accoutName) {
		this.accoutName = accoutName;
	}
	public String getAccoutDesc() {
		return accoutDesc;
	}
	public void setAccoutDesc(String accoutDesc) {
		this.accoutDesc = accoutDesc;
	}
	public Double getAmount() {
		return amount;
	}
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	
}
